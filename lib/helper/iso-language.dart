  String getName(key) {
    if (isoLanguages.containsKey(key)) {
      Map<String, String> language = isoLanguages[key];
      return language["name"] ?? "";
    } else {
      return "";
    }
  }

  /// This method return the emoji country by [key]
  String getEmoji(key) {
    if (isoLanguages.containsKey(key)) {
      Map<String, String> language = isoLanguages[key];
      return language["emoji"] ?? "🌐";
    } else {
      return "🌐";
    }
  }
